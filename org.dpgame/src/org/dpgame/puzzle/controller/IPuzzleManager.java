package org.dpgame.puzzle.controller;

import java.util.ArrayList;

import org.dpgame.puzzle.model.IPuzzleGeneratorBuilder;
import org.dpgame.puzzle.model.components.ISolutionPart;

public interface IPuzzleManager {

	public abstract void setSelectedPuzzle(IPuzzleGeneratorBuilder puzzleGeneratorBuilder, String puzzleType, int level);

	/**
	 * Generates a new puzzle of the current level using puzzle manager and
	 * displays it.
	 */
	public abstract void constructNewPuzzle();

	public abstract void viewMenu();

	public abstract void viewHelp(String url);

	/**
	 * Runs the solution generated by the player. If the solution solves the
	 * puzzle, a new puzzle with the same level is generated. If solution does
	 * not solve the puzzle, displays the current puzzle in its initial form.
	 */
	public abstract void run(ArrayList<ISolutionPart> solutionParts);

}